def summation(a, b):
    """
    Function adds two inputs a and b
    
    """
    result = a + b
    return result

def subtraction(a, b):
    """
    Function subtracts input b from a
    
    """
    result = a - b
    return result

def multiplication(a, b):
    """
    Function multipliers two input a and b
    
    """
    result = a * b
    return result